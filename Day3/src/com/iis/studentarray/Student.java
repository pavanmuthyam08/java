package com.iis.studentarray;

public class Student {
	private int sid;
	private String sname;
	private int score;
	public void setSid(int sid) {
		this.sid=sid;
	}
	public int getSid() {
	 return this.sid;	
	}
	public void setSname(String sname) {
		this.sname=sname;	
	}
	public String getSname() {
		return this.sname;
	}
	public void setScore(int score) {
		this.score=score;
	}
	public int getScore() {
		return this.score;
	}

}
